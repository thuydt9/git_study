package Day5.Test10;

public class Department {
     String idD;
    String nameD;
    String descriptionD;

    public Department(String idD, String nameD, String descriptionD) {
        this.idD = idD;
        this.nameD = nameD;
        this.descriptionD = descriptionD;
        this.idD = idD;
    }


    public Department() {

    }

    public String getIdD() {
            return idD;
        }

        public void setIdD(String idD) {
            this.idD = idD;
        }

        public String getNameD() {
            return nameD;
        }

        public void setNameD(String nameD) {
            this.nameD = nameD;
        }

        public String getDescriptionD() {
            return descriptionD;
        }

        public void setDescriptionD(String descriptionD) {
            this.descriptionD = descriptionD;
        }
    }
